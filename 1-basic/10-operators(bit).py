# & -------> and
q = 10
w = 22
print(bin(q))
print(bin(w))
print("&:",q & w)

print(40 * "-")

# | -------> or
print(bin(q))
print(bin(w))
print("|:",q | w)

print(40 * "-")

# ^ -------> xor
print(bin(q))
print(bin(w))
print("^:",q ^ w)

print(40 * "-")

# ~ -------> not
print(bin(q))
print(bin(w))
print("~:",q)
print("~:",w)

print(40 * "-")

# >> -------> shift left
print(bin(q))
print(">>:",q >> 2)

print(40 * "-")

# << -------> shift right
print(bin(q))
print("<<:",q << 2)


